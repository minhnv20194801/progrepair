public class Counter {

    private int count;

    public Counter() {
        this.count = count;
    }

    public Counter(int initial) {
        count = 0;
        int count = initial;
        this.count = count;
    }

    public void increment() {
        count++;
    }

    public void clear() {
        count = 0;
    }

    public int getCount() {
        return count;
    }

    public void setCount(int count) {
        this.count = count;
    }
}
